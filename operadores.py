"""
Operadores matemáticas (Aritméticas):
    - Soma: +
    - Subtração: -
    - Multiplicação: *
    - Divisão: /
    - Divisão inteira: //
    - Resto da Divisão: %
    - Potência: ** 
"""

numero1 = 10
numero2 = 20

print(numero1+numero2)
print(numero1-numero2)
print(numero1*numero2)
print(numero1/numero2)
print(numero1//numero2)
print(numero1%numero2)
print(numero1**numero2)

# Operadores Booleanos
idade1 = 10
idade2 = 15
idade3 = 10.0
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(42 == '42') #False
print(idade1*idade2 != idade3*idade2) #False
print(idade1 == idade3) #True
print(idade1>idade2) #False
print(idade1 <= idade2) #True
print("Python" == "python") #False
print("banana" != "abacaxi") #True
print(altura1 >= altura2) #True
print(altura2 > altura3) #False